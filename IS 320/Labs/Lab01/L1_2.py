'''
Inputs two integers, one after another
displays the results of adding, subtracting,
dividing, mult, int div, reminder, expnent.
'''
# Define Variables
first_num = 2
second_num = 3

# Computations
add = first_num + second_num
subtract = first_num - second_num
mult = first_num * second_num
div = first_num / second_num
int_div = first_num // second_num
remainder = first_num % second_num
expon = first_num ** second_num

print("Addition:", add)
print("Subtraction:", subtract)
print("Multiplication:", mult)
print("Division:", div)
print("Integer Division:", int_div)
print("Remainder:", remainder)
print("Exponentation:", expon)
